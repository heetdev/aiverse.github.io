// Mobile menu toggle
const mobileMenuBtn = document.getElementById("mobileMenuBtn")
const navLinks = document.getElementById("navLinks")

mobileMenuBtn.addEventListener("click", () => {
  mobileMenuBtn.classList.toggle("active")
  navLinks.classList.toggle("active")
})

// Close mobile menu when link is clicked
navLinks.querySelectorAll("a").forEach((link) => {
  link.addEventListener("click", () => {
    mobileMenuBtn.classList.remove("active")
    navLinks.classList.remove("active")
  })
})

// Close mobile menu when clicking outside
document.addEventListener("click", (e) => {
  if (!e.target.closest("nav")) {
    mobileMenuBtn.classList.remove("active")
    navLinks.classList.remove("active")
  }
})

document.querySelectorAll(".nav-link").forEach((link) => {
  link.addEventListener("click", (e) => {
    const href = link.getAttribute("href")

    // Close mobile menu if open
    const mobileMenu = document.getElementById("navLinks")
    if (mobileMenu.classList.contains("active")) {
      mobileMenu.classList.remove("active")
    }

    if (href === "#about") {
      e.preventDefault()
      showAboutPage()
      window.scrollTo({ top: 0, behavior: "smooth" })
    } else if (href === "#tools") {
      e.preventDefault()
      showMainPage()
      // Scroll to tools section after showing main page
      setTimeout(() => {
        const toolsSection = document.getElementById("tools")
        if (toolsSection) {
          toolsSection.scrollIntoView({ behavior: "smooth" })
        }
      }, 100)
    } else if (href === "#home") {
      e.preventDefault()
      showMainPage()
      // Scroll to top for home
      window.scrollTo({ top: 0, behavior: "smooth" })
    }
  })
})

function showMainPage() {
  document.getElementById("mainPage").style.display = "block"
  document.getElementById("aboutPage").classList.remove("active")
  document.getElementById("detailPage").classList.remove("active")
}

function showAboutPage() {
  document.getElementById("mainPage").style.display = "none"
  document.getElementById("aboutPage").classList.add("active")
  document.getElementById("detailPage").classList.remove("active")
}

// Filter tools by category
function filterTools(category) {
  const cards = document.querySelectorAll(".tool-card")
  cards.forEach((card) => {
    if (category === "all" || card.dataset.category === category) {
      card.style.display = "block"
    } else {
      card.style.display = "none"
    }
  })
}

// Category button click handlers
document.querySelectorAll(".category-btn").forEach((btn) => {
  btn.addEventListener("click", function () {
    document.querySelectorAll(".category-btn").forEach((b) => b.classList.remove("active"))
    this.classList.add("active")
    filterTools(this.dataset.category)
  })
})

// Tool card click handlers
document.querySelectorAll(".tool-card").forEach((card) => {
  card.addEventListener("click", function (e) {
    e.preventDefault()
    const toolId = this.dataset.tool
    const tool = toolsData[toolId]

    if (tool) {
      document.getElementById("detailIcon").textContent = tool.icon
      document.getElementById("detailTitle").textContent = tool.name
      document.getElementById("detailCategory").textContent = tool.category
      document.getElementById("detailDescription").innerHTML = tool.description

      document.getElementById("detailPricing").textContent = tool.pricing
      document.getElementById("detailBestFor").textContent = tool.bestFor
      document.getElementById("detailEmail").textContent = tool.email

      document.getElementById("tryBtn").href = tool.url

      document.getElementById("mainPage").style.display = "none"
      // Updated to also hide aboutPage when showing detailPage
      document.getElementById("aboutPage").classList.remove("active")
      document.getElementById("detailPage").classList.add("active")

      // Scroll to top of detail page
      window.scrollTo({ top: 0, behavior: "smooth" })
    }
  })
})

